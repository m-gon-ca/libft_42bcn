#include <unistd.h>

void	*ft_bzero(void *s, size_t n)
{
	int	i;

	i = 0;
	while ((((unsigned char*)s)[i] != '\0') && n > 0)
	{
		((unsigned char*)s)[i] = '\0';
		i++;
		n--;
	}
	return (s);
}
